						<h4>- Apa itu Caraka -</h4>
						<p>Caraka adalah sebuah festival kreatif khusus mahasiswa se-Indonesia.</p>
						<h4>- Filosofi Caraka -</h4>
						<p>Kenapa kata caraka yang muncul menjadi nama festival ini? Caraka Festival Kreatif mendasarkan pada filosofi pewayangan Jawa, dimana Caraka berarti "Utusan". Dalam tokoh pewayangan Jawa, Prabu Kresna merupakan representasi dari utusan. Dikisahkan bahwa dalam setiap perang terbuka melawan Kurawa, maka Pandawa akan selalu mengirim Prabu Kresna sebagai duta atau utusan. Sebagai tokoh yang berwibawa, Kresna juga dikenal banyak akal. Hal ini menjadi dasar konsep Kreatifitas dari Caraka sebagai sebuah Festival Kreatif.
							Seorang mahasiswa, secara hirarki adalah seorang utusan. Utusan dari keluarga, dari daerahnya, dari lingkungan dia tumbuh, sampai di titik bangku kuliah. Siapapun mahasiswa diharapkan menjadi utusan terbaik dimana dia berada. Dari spirit itu juga akhirnya nama Caraka muncul dan menjadi nama gelaran festival kreatif khusus mahasiswa se-Indonesia.</p>
						<h4>- Sejarah Caraka -</h4>
						<p>Berawal dari semangat para mahasiswa pecinta dunia kreatif Semarang, Playon Kreadtiv sebagai komuntias kreatif Semarang mempunyai ide untuk membuat sebuah festival kreatif khusus mahasiswa Indonesia karena belum ada sebuah festival kreatif yang khusus mewadahi potensi kreatif mahasiswa Indonesia.
							Tahun pertama caraka digawangi oleh HMJ Ilmu Komunikasi FISIP UNDIP & Playon Kreadtiv, dan berkolaborasi dengan DKV Universitas Katolik Soegijapranata. Ide perhelatan besar ini disupport penuh oleh PPPI (Persatuan Perusahaan Periklanan Indonesia) Pengda Jateng, dan semua sepakat untuk bersama-sama saling support dan mengembangkan festival kreatif ini.
							Akhirnya Caraka Festival Kreatif berhasil kick-off pada tanggal 18, 19, 20 Juni 2009. INDONESIA TELAH MENJADI SAKSI PESTA INI & MAHASISWA INDONESIA TELAH MENGAKUINYA.
						</p>
						<h4>- Karakter Caraka -</h4>
						<p>Festival kreatif khusus mahasiswa se-Indonesia, sehingga semua yang ada dalam program Caraka akan diarahakan kepada kebutuhan mahasiswa dan industri periklanan.</p>
						<h4>- Visi Caraka -</h4>
						<p>Sebuah Festival Kreatif Khusus Mahasiswa yang bertujuan Membuka wawasan, Membuktikan dan Menyiapkan mahasiswa ke Indutri Periklanan. </p>
